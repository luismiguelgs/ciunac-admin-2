"use client"

import * as React from "react"
import { format, getMonth, getYear, setMonth, setYear } from "date-fns"
import { Calendar as CalendarIcon } from "lucide-react"
import { es } from "date-fns/locale";
import { cn } from "@/lib/utils"
import { Button } from "@/components/ui/button"
import { Calendar } from "@/components/ui/calendar"
import {
    Popover,
    PopoverContent,
    PopoverTrigger,
} from "@/components/ui/popover"
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from "@/components/ui/select"
import { Field, FieldContent, FieldDescription, FieldError, FieldLabel } from "@/components/ui/field"
import { Control, Controller, FieldValues, Path } from "react-hook-form"

interface DatePickerProps<T extends FieldValues> {
    control: Control<T>
    name: Path<T>
    label: string
    description?: string
    startYear?: number
    endYear?: number
    disabled?: boolean
    orientation?: "vertical" | "horizontal" | "responsive"
    className?: string
}

export function DatePicker<T extends FieldValues>({
    control,
    name,
    disabled = false,
    label,
    description,
    startYear = getYear(new Date()) - 80,
    endYear = getYear(new Date()) + 0,
    orientation = "vertical",
    className,
}: DatePickerProps<T>) {
    const months = [
        "Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio",
        "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"
    ]

    const years = Array.from(
        { length: endYear - startYear + 1 },
        (_, i) => startYear + i
    )

    const [currentMonth, setCurrentMonth] = React.useState<Date>(new Date())
    const [open, setOpen] = React.useState(false)

    return (
        <Controller
            control={control}
            name={name}
            render={({ field, fieldState }) => (
                <Field
                    orientation={orientation}
                    data-invalid={fieldState.invalid}
                    className={className}
                >
                    <FieldLabel htmlFor={`date-${name}`}>{label}</FieldLabel>
                    <FieldContent>
                        <Popover
                            open={open}
                            onOpenChange={(v: boolean | ((prevState: boolean) => boolean)) => {
                                setOpen(v)
                                if (v) {
                                    setCurrentMonth(field.value || new Date())
                                }
                            }}
                        >
                            <PopoverTrigger asChild>
                                <Button
                                    id={`date-${name}`}
                                    disabled={disabled}
                                    variant={"outline"}
                                    className={cn(
                                        "w-full justify-start text-left font-normal",
                                        !field.value && "text-muted-foreground",
                                        fieldState.invalid && "border-destructive focus-visible:ring-destructive"
                                    )}
                                >
                                    <CalendarIcon className="mr-2 h-4 w-4" />
                                    {field.value ? format(field.value, "dd/MM/yyyy") : <span>Seleccionar fecha</span>}
                                </Button>
                            </PopoverTrigger>
                            <PopoverContent className="w-auto p-0">
                                <div className="flex justify-between p-2">
                                    <Select
                                        onValueChange={(month) => {
                                            const base = field.value || currentMonth || new Date()
                                            const newDate = setMonth(base, months.indexOf(month))
                                            setCurrentMonth(newDate)
                                            field.onChange(newDate)
                                        }}
                                        value={months[getMonth(currentMonth)]}
                                    >
                                        <SelectTrigger className="w-[110px]">
                                            <SelectValue placeholder="Mes" />
                                        </SelectTrigger>
                                        <SelectContent>
                                            {months.map(month => (
                                                <SelectItem key={month} value={month}>{month}</SelectItem>
                                            ))}
                                        </SelectContent>
                                    </Select>
                                    <Select
                                        onValueChange={(year) => {
                                            const base = field.value || currentMonth || new Date()
                                            const newDate = setYear(base, parseInt(year))
                                            setCurrentMonth(newDate)
                                            field.onChange(newDate)
                                        }}
                                        value={getYear(currentMonth).toString()}
                                    >
                                        <SelectTrigger className="w-[110px]">
                                            <SelectValue placeholder="AÃ±o" />
                                        </SelectTrigger>
                                        <SelectContent>
                                            {years.map(year => (
                                                <SelectItem key={year} value={year.toString()}>{year}</SelectItem>
                                            ))}
                                        </SelectContent>
                                    </Select>
                                </div>

                                <Calendar
                                    mode="single"
                                    selected={field.value}
                                    onSelect={(date) => {
                                        if (date) setCurrentMonth(date)
                                        field.onChange(date)
                                    }}
                                    onMonthChange={setCurrentMonth}
                                    month={currentMonth}
                                    initialFocus
                                    //weekStartsOn={0}
                                    locale={es}
                                />

                            </PopoverContent>
                        </Popover>
                        {description && (
                            <FieldDescription>
                                {description}
                            </FieldDescription>
                        )}
                        {fieldState.invalid && (
                            <FieldError errors={[fieldState.error]} />
                        )}
                    </FieldContent>
                </Field>
            )}
        />
    )
}